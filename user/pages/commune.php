    <div class="card-body m-5">
        <h2 class="card-title">Historia de Concepcion</h2>
        <hr>
        <h5 class="text-start"> Fundada el 5 de Octubre de 1550 por Pedro de Valdivia, la ciudad se encontraba primero en Penco, donde aún hoy se halla el actual fuerte. Entre los años 1565 y 1573 fue sede de la Real Audiencia y Capital de la Capitanía General de Chile.</h5>
        <br>
        <h5 class="text-start">Bernardo O Higgins, Juan Martínez de Rosas, Ramón Freire, Joaquín Prieto y Manuel Bulnes son algunos de los habitantes oriundos de la ciudad que tuvieron un rol primordial y protagonista en la Independencia del país.</h5>
        <br>
        <h5 class="text-start">En 1751, un maremoto destruyó la ciudad que hasta ese entonces todavía se encontraba emplazada en Penco, lo que significó volver a construirla pero esta vez trasladando su emplazamiento al actual, el Valle de la Rosa. Esto ocurrió en el año 1764, y recién en 1786 el gobierno impuso el régimen de Intendencias y se fundó así la Intendencia de Concepción.</h5>
        <br>
        <h5 class="text-start">La ciudad volvió a ser alcanzada esta vez por los terribles terremotos de 1939 y 1960, que la destruyeron totalmente.</h5>
        <br>
        <h5 class="text-start">El 27 de febrero de 2010 se produjo un terremoto de magnitud 8,8 en las escala ritcher, con epicentro a 90 kilómetros al noroeste de la ciudad, causando numerosos daños materiales y un total de 524 muertos. Pero volvió a levantarse de sus cimientos para convertirse en una hermosa y moderna ciudad marina, donde el desarrollo económico y el crecimiento se reflejan todos los días del año.</h5>
        <br>
        <h5 class="text-start">Actualmente vive una explosión demográfica, cultural e inmobiliaria que ha experimentado, y que la mantiene como una de las ciudades más importantes de Chile.</h5>
        

